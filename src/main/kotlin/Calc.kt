class Calc {
    companion object{
        fun soma(num1: Double, num2: Double): Double{
            return num1 + num2
        }
        fun subtracao(num1: Double, num2: Double): Double{
            return num1 - num2
    }
        fun divisao(num1: Double, num2: Double): Double{
        return num1 / num2
        }

        fun mutiplicacao(num1: Double, num2: Double): Double{
            return num1 * num2
        }
    }
}